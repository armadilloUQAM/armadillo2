    private void enabledFunctionForFS_Advanced_Options(boolean e) {
        this.FAO__score_Box.setEnabled(e);
        if (properties.isSet(FAO__score_Box.getName()) && e==true) {
            this.FAO__score_Box_IntValue.setEnabled(true);
        } else {
            this.FAO__score_Box_IntValue.setEnabled(false);
        }

        this.FAO__mfe_Box.setEnabled(e);
        if (properties.isSet(FAO__mfe_Box.getName()) && e==true) {
            this.FAO__mfe_Box_DouValue.setEnabled(true);
        } else {
            this.FAO__mfe_Box_DouValue.setEnabled(false);
        }

    }
    

    /*******************************************************************
    * Update Saved Properties => usp_functions
    *******************************************************************/

    private void updateSavedProperties(workflow_properties properties) {
        usp_valueANDtext (properties);
        usp_boxANDbutton (properties);
    }
    
    private void usp_valueANDtext (workflow_properties properties) {
        if (properties.isSet(FAO__score_Box_IntValue.getName())){
            this.FAO__score_Box_IntValue.setValue(Integer.parseInt(properties.get(FAO__score_Box_IntValue.getName())));
            this.FAO__score_Box_IntValue.setEnabled(false);
        }
        if (properties.isSet(FAO__mfe_Box_DouValue.getName())){
            this.FAO__mfe_Box_DouValue.setValue(Double.parseDouble(properties.get(FAO__mfe_Box_DouValue.getName())));
            this.FAO__mfe_Box_DouValue.setEnabled(false);
        }
    }
    
    private void usp_boxANDbutton (workflow_properties properties) {
        if (properties.isSet(FAO__score_Box.getName())){
            this.FAO__score_Box.setSelected(true);
            this.FAO__score_Box_IntValue.setEnabled(true);
        }
        if (properties.isSet(FAO__mfe_Box.getName())){
            this.FAO__mfe_Box.setSelected(true);
            this.FAO__mfe_Box_DouValue.setEnabled(true);
        }
    }
    

    /*******************************************************************
     * Set the configuration properties for this object
     ******************************************************************/

    @Override
    public void display(workflow_properties properties) {
        this.properties=properties;
        initComponents();
        setIconImage(Config.image);
        // Set position
        Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();
        Dimension d = getSize();
        setLocation((screenSize.width-d.width)/2,
                (screenSize.height-d.height)/2);
        
        // Set the program properties
        this.setProperties(properties);
        
        // Update Saved Properties => usp
        this.updateSavedProperties(properties);
        
        this.setAlwaysOnTop(true);
        this.setVisible(true);
    }

